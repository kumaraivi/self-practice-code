package practice;

public class SwitchStatment {

	public static void main(String[] args) {
		int choice = 2;

		switchStatement(choice);

	}

	private static void switchStatement(int choice) {

		switch (choice) {
		case 1: System.out.println("In am in case 1");
		case 2: System.out.println("In am in case 2");
			
			}
		}

}
